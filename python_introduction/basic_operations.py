# This script demonstrates basic mathematical operations in Python.
number1 = 10
number2 = 5
# Define two numbers for our calculations
print(f"Numbers chosen for calculations: number1 = {number1}, number2 = {number2}\n")

# 1. Addition (+)
result_addition = number1 + number2
print(f"Addition of {number1} and {number2} is {result_addition}")

# 2. Subtraction (-)
result_subtraction = number1 - number2
print(f"Subtraction of {number1} and {number2} is {result_subtraction}")

# 3. Multiplication (*)
result_multiplication = number1 * number2
print(f"Multiplication of {number1} and {number2} is {result_multiplication}")